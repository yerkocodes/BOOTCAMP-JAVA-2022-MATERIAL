package com.yerko.model;

public class Calculadora {
    public float restar(Float a, Float b) {
        return a - b;
    }

    public float sumar(Float a, Float b) {
        return a + b;
    }

    public float multiplicar(Float a, Float b) {
        return a * b;
    }

    public float dividir(Float a, Float b) {
        return a / b;
    }
}
